void solve(){
    int n,m;
    cin>>n>>m;
    int c,ans=0;
    if(n>2){
        c=n/3;
        n%=3;
        ans+=c*m;
    }
    if(m>2){
        c=m/3;
        m%=3;
        ans+=c*n;
    }
        
    ans+=(n*m==1? 1:((n*m)/2));
    cout<<ans<<endl;
}
